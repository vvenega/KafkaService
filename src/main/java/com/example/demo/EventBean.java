package com.example.demo;

import java.util.Date;

public class EventBean {
	
	private String owner;
	private Double price;
	private String category;
	private String type;
	private String name;
	private String user;
	private long objectid;
	private String event;
	private String datetime;
	private boolean viewclick;
	private boolean contactclick;
	
	public String getOwner() {
		return owner;
	}
	public void setOwner(String owner) {
		this.owner = owner;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public long getObjectid() {
		return objectid;
	}
	public void setObjectid(long objectid) {
		this.objectid = objectid;
	}
	public String getEvent() {
		return event;
	}
	public void setEvent(String event) {
		this.event = event;
	}
	public String getDatetime() {
		return datetime;
	}
	public void setDatetime(String datetime) {
		this.datetime = datetime;
	}
	public boolean isViewclick() {
		return viewclick;
	}
	public void setViewclick(boolean viewclick) {
		this.viewclick = viewclick;
	}
	public boolean isContactclick() {
		return contactclick;
	}
	public void setContactclick(boolean contactclick) {
		this.contactclick = contactclick;
	}
	
	


}
